    private void itemDesasignarSalaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itemDesasignarSalaActionPerformed
        // TODO add your handling code here:
        ArrayList<ObraDTeatro> validar = new ArrayList();

        for (int i = 0; i < obras.size(); i++) {
            if (obras.get(i).getSalaAsignada() != null) {
                validar.add(obras.get(i));
            }
        }

        if (validar.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No hay registro de obras con salas asignadas");
        } else {
            DesasignarSala ds = new DesasignarSala();

            escritorio.add(ds);
            ds.show();
        }

    }//GEN-LAST:event_itemDesasignarSalaActionPerformed

